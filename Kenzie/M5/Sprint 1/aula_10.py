# GITIGNORE

# o git nos permite versionar e subir um projeto para o repositório,
# porém é uma boa prática sabermos que existe arquivos e diretórios,
# que não são necessários estarem no repositório.


# * __pycache__ *

# são os arquivos de cache e são gerados pela importações da nossa aplicação.

# é um diretório gerado automaticamente pelo Python na execução do projeto,
# ele é um diretório que contem arquivos bytecodes, que deixam o código mais 
# otimizado para ser rodado localmente.


